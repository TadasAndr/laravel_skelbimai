    {
        $input = $request->except('_token');
        if($file = $request->file('picture')) {
            $name = uniqid() . $file->getClientOriginalName();
            $file->move('images', $name);
            $input['picture'] = $name;
        }
        Ad::query()->create($input);

        return $input;
    }

